function [model]= mars_build_model(MaxBasis,trainParams)
%build a MARS : get the structure right
model = struct('MSE',Inf,'GCV',Inf,'coefs',zeros(MaxBasis+1,1),'isBinary',false(1,MaxBasis),'knotdims',...
     zeros(MaxBasis,1),'knotsites',zeros(MaxBasis,1),'knotdirs',...
     zeros(MaxBasis,1),'trainParams',trainParams,'parents',zeros(MaxBasis,1),...
     'minX',zeros(1,MaxBasis),'maxX',zeros(1,MaxBasis));
end